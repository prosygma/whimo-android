package com.whimo.presentation.createtransaction.components

import android.util.Log
import androidx.annotation.OptIn
import androidx.camera.core.ExperimentalGetImage
import androidx.camera.core.ImageAnalysis
import androidx.camera.core.ImageProxy
import com.google.mlkit.vision.barcode.BarcodeScannerOptions
import com.google.mlkit.vision.barcode.BarcodeScanning
import com.google.mlkit.vision.barcode.common.Barcode
import com.google.mlkit.vision.common.InputImage
import java.nio.charset.Charset

class QrAnalyzer(
    private val onDataScanned: (String) -> Unit,
) : ImageAnalysis.Analyzer {
    private val scanner = BarcodeScanning.getClient(
        BarcodeScannerOptions.Builder()
            .setBarcodeFormats(Barcode.FORMAT_QR_CODE)
            .build()
    )

    @OptIn(ExperimentalGetImage::class)
    override fun analyze(imageProxy: ImageProxy) {
        val mediaImage = imageProxy.image ?: run {
            imageProxy.close()
            return
        }

        val image = InputImage.fromMediaImage(mediaImage, imageProxy.imageInfo.rotationDegrees)

        scanner.process(image)
            .addOnSuccessListener { barcodes ->
                barcodes.firstOrNull()?.rawBytes?.let { bytes ->
                    onDataScanned(decodeRawBytes(bytes))
                }
            }
            .addOnFailureListener { e ->
                Log.e("QrAnalyzer", "error: ${e.message}")
            }
            .addOnCompleteListener {
                imageProxy.close()
            }
    }

    private fun decodeRawBytes(bytes: ByteArray): String {
        return try {
            val utf8 = bytes.toString(Charsets.UTF_8)
            if (utf8.contains('\uFFFD')) {
                bytes.toString(Charset.forName("ISO-8859-1"))
            } else {
                utf8
            }
        } catch (e: Exception) {
            bytes.toString(Charsets.UTF_8)
        }
    }
}